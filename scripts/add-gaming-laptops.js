function makeHistory(base) {
  const history = [];
  for (let i = 30; i >= 0; i--) {
    const d = new Date('2026-02-28');
    d.setDate(d.getDate() - i);
    const date = d.toISOString().split('T')[0];
    const variance = Math.round((Math.random() - 0.5) * base * 0.04);
    history.push({ date, price: base + variance });
  }
  return history;
}

function makeShops(base) {
  return shops.map((s, i) => ({
    ...s,
    price: base + Math.round((i - 1.5) * base * 0.015),
  }));
}

